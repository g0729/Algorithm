WITH TEMP AS (SELECT
    DISTINCT(B.ITEM_ID)
FROM
    ITEM_TREE B
LEFT JOIN
    ITEM_TREE R
ON
    B.ITEM_ID = R.PARENT_ITEM_ID
WHERE R.ITEM_ID IS NULL
)

SELECT
    B.ITEM_ID,
    ITEM_NAME,
    RARITY
FROM 
    TEMP B
JOIN
    ITEM_INFO R
ON
    B.ITEM_ID = R.ITEM_ID
ORDER BY B.ITEM_ID DESC
    